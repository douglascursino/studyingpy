# Criando uma lista de nomes
nomes = ["Maria", "José", "João", "Ana", "Pedro"]
# Ordenando a lista em ordem alfabética
nomes.sort()
# Imprimindo a lista
print(nomes)
minha_lista = [8, 10, 6, 2, 4]
minha_lista.sort()
print(minha_lista)
# Ordenando a lista em ordem decrescente
numeros_ordenados_decrescente = sorted(minha_lista, reverse=True)
lista_nomes = sorted(nomes, reverse=True)
# Imprimindo o resultado
print("A lista em ordem decrescente é:", numeros_ordenados_decrescente)
print("A lista de nomes em ordem decrescente é:", lista_nomes)
# Ordenando a lista em ordem decrescente usando sort()
minha_lista.sort(reverse=True)
print("A lista em ordem decrescente é:", minha_lista)
nomes.sort(reverse=True)
print("A lista de nomes em ordem decrescente é:", nomes)