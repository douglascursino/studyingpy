usuario_palavra = input("Entre com uma palavra: ")
usuario_palavra = usuario_palavra.upper()
for letra in usuario_palavra:
    if letra == "A\n":
        continue
    elif letra == "E\n":
        continue
    elif letra == "I\n":
        continue
    elif letra == "O\n":
        continue
    elif letra == "U\n":
        continue
    else:
        print(letra)
    